// Helper functions
export function getAllTavoites(): Tavoite[] {
  return tavoiteVocabulary;
}

export function getTavoiteById(id: number): Tavoite | undefined {
  return tavoiteVocabulary.find(t => t.id === id);
}

export function getWordsForTavoites(tavoiteIds: number[]): VocabularyWord[] {
  return tavoiteVocabulary
    .filter(t => tavoiteIds.includes(t.id))
    .flatMap(t => t.words);
}

export function getTotalWordCount(): number {
  return tavoiteVocabulary.reduce((sum, t) => sum + t.words.length, 0);
}

export function getWordCountForTavoites(tavoiteIds: number[]): number {
  return tavoiteVocabulary
    .filter(t => tavoiteIds.includes(t.id))
    .reduce((sum, t) => sum + t.words.length, 0);
}

